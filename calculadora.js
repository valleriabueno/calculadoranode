module.exports = {
    
    soma(a, b) {
        let resultado = a + b
        return resultado
    },
    subtracao(a, b) {
        let resultado = a - b
        return resultado
    },
    multiplicacao(a, b) {
        let resultado = a * b
        return resultado
    },
    divisao(a, b) {
        let resultado = (a / b)
        return resultado
    }
}